package ro.unibuc.laborator.lab2;

public class ImmutablePoint {

    private int x, y; //-> no access modifier; can only be accessed from the same package

    public ImmutablePoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
